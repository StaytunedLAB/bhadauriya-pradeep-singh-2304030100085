/****************************************************
 * Chapter 08: Loops
 * Description: for, while, do..while
 ****************************************************/

for (let i = 1; i <= 5; i++) console.log("for:", i)

let j = 1
while (j <= 3) {
  console.log("while:", j)
  j++
}

let k = 1
do {
  console.log("do..while:", k)
  k++
} while (k <= 2)
